#include <iostream>

using namespace std;

int main()
{
  auto x = 3.2;
  cout << "x = " << x << endl;
  
  auto f = true;
  cout << "f = " << f << endl;
  
  auto g = 0x111;
  cout << "g = " << g << endl;

  auto b = 0b0101;
  cout << "b = " << b << endl;

  auto str1 = "Fadjar Fathurrahman";
  cout << "str1 = " << str1 << endl;

  auto chr1 = 'a';
  cout << "chr1 = " << chr1 << endl;

  return 0;
}
